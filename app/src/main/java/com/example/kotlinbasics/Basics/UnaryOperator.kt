package com.andr.firstproject

fun main(args: Array<String>) {

    var number:Double = 7.6
    var isCheck:Boolean = true

    println("=number = " + +number)
    println("-number = " + -number)
    println("++number = " + ++number)
    println("-number = " + --number)
    println("!isCheck = " + !isCheck)

    println("-----------------------------------------")

    var age:Double = 4.7
    println(age)
    println(age++)
    println(age)
    println(++age)
    println(age)

}